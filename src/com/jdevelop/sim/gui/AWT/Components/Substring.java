package com.jdevelop.sim.gui.AWT.Components;

class Substring {
    public int begin;
    public int length;
    public int xOff;
    public int yOff;

    public Substring( int begin, int length, int xOff, int yOff ) {
        this.begin = begin;
        this.length = length;
        this.xOff = xOff;
        this.yOff = yOff;
    }
}
